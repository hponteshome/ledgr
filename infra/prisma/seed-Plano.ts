// Funções de suporte (mesmas do anterior...)
function getAccountType(digit: string): AccountType {
  if (digit === '1') return AccountType.ASSET;
  if (digit === '2') return AccountType.LIABILITY;
  if (digit === '3') return AccountType.REVENUE;
  return AccountType.EXPENSE;
}

function getAccountNature(digit: string, name: string): AccountNature {
  const isRedutora = name.includes('(-)');
  if (digit === '1' || digit === '4') return isRedutora ? AccountNature.CREDIT : AccountNature.DEBIT;
  return isRedutora ? AccountNature.DEBIT : AccountNature.CREDIT;
}